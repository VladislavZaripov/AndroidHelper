    @Override
    public boolean onCreateOptionsMenu(Menu menu) {
        getMenuInflater().inflate(R.menu.main_menu,menu);
        menu.add(0,1010101,0,"show item 2 & 3").setCheckable(true);
        ActionBar actionbar = getSupportActionBar();
        actionbar.setTitle("Main screen");
        return true;
    }

    @Override
    public boolean onPrepareOptionsMenu(Menu menu) {
        menu.setGroupVisible(R.id.menu_group1,menu.findItem(1010101).isChecked());
        ActionBar actionbar = getSupportActionBar();
        actionbar.setTitle("Main screen");
        actionbar.setDisplayOptions(ActionBar.DISPLAY_SHOW_HOME | ActionBar.DISPLAY_SHOW_TITLE );
        // actionbar.setIcon(R.drawable.utilities_tools2);
        return super.onPrepareOptionsMenu(menu);
    }

    @Override
    public boolean onOptionsItemSelected(@NonNull MenuItem item) {
        switch (item.getItemId()){
            case R.id.menu_settings:
                Toast.makeText(this, "menu_settings",Toast.LENGTH_LONG).show();
                return true;
            case R.id.menu_item1:
                Toast.makeText(this, "menu_item1",Toast.LENGTH_LONG).show();
                return true;
            case R.id.menu_item2:
                Toast.makeText(this, "menu_item2",Toast.LENGTH_LONG).show();
                return true;
            case R.id.menu_item3:
                Toast.makeText(this, "menu_item3",Toast.LENGTH_LONG).show();
                return true;
            case 1010101:
                item.setChecked(!item.isChecked());
                Toast.makeText(this, "show item 2 & 3",Toast.LENGTH_LONG).show();
                return true;
        }
        return super.onOptionsItemSelected(item);
    }

